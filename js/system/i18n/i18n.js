if (typeof window !== 'undefined') {
  window.getSafeTranslation = function (lang, key) {
    try {
      const pack = I18N[lang] || I18N['ua'];
      const translation = pack[key];

      if (translation !== undefined) {
        return translation;
      }

      const fallbackLangs = ['ua', 'ru', 'en'];
      for (const fallbackLang of fallbackLangs) {
        if (fallbackLang !== lang) {
          const fallbackTranslation = I18N[fallbackLang][key];
          if (fallbackTranslation !== undefined) {
            ErrorHandler.debug('Translation fallback used', {
              key,
              requestedLang: lang,
              fallbackLang: fallbackLang,
            });
            return fallbackTranslation;
          }
        }
      }

      ErrorHandler.warn('Translation not found', { lang, key });
      return I18N['ua']['no_translation'] || key;
    } catch (error) {
      ErrorHandler.warn('Translation getter failed', { lang, key, error });
      return key;
    }
  };
}
